    private void initTableModel() {
        tableModel = new DefaultTableModel(new String[]{"ID", "Tanggal", "Person", "Jenis", "Jumlah", "Keterangan"}, 0) {
            @Override
            public boolean isCellEditable(int row, int column) { return false; }
        };
        tblDataTransaksi.setModel(tableModel);
        tblDataTransaksi.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }

    private void initListeners() {
        tblDataTransaksi.getSelectionModel().addListSelectionListener(e -> {
            if (!e.getValueIsAdjusting()) { showSelectedDataToForm(); }
        });
    }

    private void onSimpan(ActionEvent evt) {
        Transaksi t = getTransaksiFromForm();
        if (t == null) return;

        t.setTanggal(new Date()); // Menetapkan waktu sistem saat ini secara otomatis
        if (transaksiDAO.insert(t)) {
            JOptionPane.showMessageDialog(this, "Transaksi berhasil disimpan.");
            refreshTableAfterCrud();
            resetForm();
        } else {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan transaksi.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void onEdit(ActionEvent evt) {
        Transaksi t = getTransaksiFromForm();
        if (t == null) return;

        if (transaksiDAO.update(t)) {
            JOptionPane.showMessageDialog(this, "Transaksi berhasil diubah.");
            refreshTableAfterCrud();
            resetForm();
        } else {
            JOptionPane.showMessageDialog(this, "Gagal mengubah transaksi.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void onHapus(ActionEvent evt) {
        String idText = txtIdTransaksi.getText().trim();
        if (idText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pilih data transaksi yang akan dihapus.", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus transaksi ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            int id = Integer.parseInt(idText);
            if (transaksiDAO.delete(id)) {
                JOptionPane.showMessageDialog(this, "Transaksi berhasil dihapus.");
                refreshTableAfterCrud();
                resetForm();
            } else {
                JOptionPane.showMessageDialog(this, "Gagal menghapus transaksi.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }

    private void onReset(ActionEvent evt) {
        resetForm();
        loadDataTable();
    }

    private void onRefresh(ActionEvent evt) {
        txtCari.setText("");
        loadComboPerson();
        loadComboJenis();
        loadDataTable();
        JOptionPane.showMessageDialog(this, "Data berhasil diperbarui.");
    }

    private void onCari(ActionEvent evt) {
        String keyword = txtCari.getText().trim();
        if (keyword.isEmpty()) {
            loadDataTable();
            return;
        }
        List<Transaksi> list = transaksiDAO.search(keyword);
        setTableRows(list);
    }

    private Transaksi getTransaksiFromForm() {
        String idText = txtIdTransaksi.getText().trim();
        String jumlahText = txtJumlah.getText().trim();
        String keterangan = txtKeterangan.getText().trim();

        if (jumlahText.isEmpty() || keterangan.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Field Jumlah dan Keterangan wajib diisi.", "Validasi", JOptionPane.WARNING_MESSAGE);
            return null;
        }

        double jumlah;
        try {
            jumlah = Double.parseDouble(jumlahText);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Jumlah harus berupa angka.", "Validasi", JOptionPane.WARNING_MESSAGE);
            return null;
        }

        if (jumlah <= 0) {
            JOptionPane.showMessageDialog(this, "Jumlah nilai transaksi harus lebih dari 0.", "Validasi", JOptionPane.WARNING_MESSAGE);
            return null;
        }

        ComboItem personItem = (ComboItem) cmbPerson.getSelectedItem();
        ComboItem jenisItem = (ComboItem) cmbJenisTransaksi.getSelectedItem();

        if (jenisItem == null) {
            JOptionPane.showMessageDialog(this, "Jenis transaksi wajib dipilih.", "Validasi", JOptionPane.WARNING_MESSAGE);
            return null;
        }

        int idTransaksi = idText.isEmpty() ? 0 : Integer.parseInt(idText);
        Integer idPerson = (personItem == null || personItem.getId() == 0) ? null : personItem.getId();

        Transaksi t = new Transaksi();
        t.setIdTransaksi(idTransaksi);
        t.setIdPerson(idPerson);
        t.setIdJenis(jenisItem.getId());
        t.setJumlah(jumlah);
        t.setKeterangan(keterangan);
        return t;
    }

    private void loadDataTable() {
        List<Transaksi> list = transaksiDAO.getAll();
        setTableRows(list);
    }

    private void setTableRows(List<Transaksi> list) {
        tableModel.setRowCount(0);
        for (Transaksi t : list) {
            tableModel.addRow(new Object[]{
                t.getIdTransaksi(),
                dateFormat.format(t.getTanggal()),
                t.getNamaPerson(),
                t.getNamaJenis(),
                currencyFormat.format(t.getJumlah()),
                t.getKeterangan()
            });
        }
    }

    private void showSelectedDataToForm() {
        int selectedRow = tblDataTransaksi.getSelectedRow();
        if (selectedRow == -1) return;

        txtIdTransaksi.setText(tableModel.getValueAt(selectedRow, 0).toString());
        
        // Parsing nominal uang dari string berformat rupiah kembali ke tipe double mentah
        try {
            Number num = currencyFormat.parse(tableModel.getValueAt(selectedRow, 4).toString());
            txtJumlah.setText(String.valueOf(num.doubleValue()));
        } catch (Exception e) {
            txtJumlah.setText("");
        }
        
        txtKeterangan.setText(tableModel.getValueAt(selectedRow, 5).toString());

        // Mensinkronkan ComboBox Visual berdasarkan nama relasi di tabel
        String namaPersonTable = tableModel.getValueAt(selectedRow, 2).toString();
        for (int i = 0; i < cmbPerson.getItemCount(); i++) {
            if (cmbPerson.getItemAt(i).toString().equals(namaPersonTable)) {
                cmbPerson.setSelectedIndex(i);
                break;
            }
        }

        String namaJenisTable = tableModel.getValueAt(selectedRow, 3).toString();
        for (int i = 0; i < cmbJenisTransaksi.getItemCount(); i++) {
            if (cmbJenisTransaksi.getItemAt(i).toString().equals(namaJenisTable)) {
                cmbJenisTransaksi.setSelectedIndex(i);
                break;
            }
        }
    }

    private void refreshTableAfterCrud() {
        String keyword = txtCari.getText().trim();
        if (keyword.isEmpty()) {
            loadDataTable();
        } else {
            List<Transaksi> list = transaksiDAO.search(keyword);
            setTableRows(list);
        }
    }

    private void resetForm() {
        txtIdTransaksi.setText(transaksiDAO.getNextId());
        txtJumlah.setText("");
        txtKeterangan.setText("");
        if (cmbPerson.getItemCount() > 0) cmbPerson.setSelectedIndex(0);
        if (cmbJenisTransaksi.getItemCount() > 0) cmbJenisTransaksi.setSelectedIndex(0);
        cmbPerson.requestFocus();
        tblDataTransaksi.clearSelection();
    }
    
