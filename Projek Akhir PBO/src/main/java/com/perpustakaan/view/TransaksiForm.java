    private void loadComboPerson() {
        cmbPerson.removeAllItems();
        cmbPerson.addItem(new ComboItem(0, "--- Umum / Tanpa Person ---"));
        String sql = "SELECT id_person, nama FROM person ORDER BY nama";
        try (Connection conn = DatabaseConnection.getConnection();
             PreparedStatement stmt = conn.prepareStatement(sql);
             ResultSet rs = stmt.executeQuery()) {
            while (rs.next()) {
                cmbPerson.addItem(new ComboItem(rs.getInt("id_person"), rs.getString("nama")));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data Person: " + e.getMessage());
        }
    }

    private void loadComboJenis() {
        cmbJenisTransaksi.removeAllItems();
        String sql = "SELECT id_jenis, nama_jenis FROM jenis_transaksi ORDER BY id_jenis";
        try (Connection conn = DatabaseConnection.getConnection();
             PreparedStatement stmt = conn.prepareStatement(sql);
             ResultSet rs = stmt.executeQuery()) {
            while (rs.next()) {
                cmbJenisTransaksi.addItem(new ComboItem(rs.getInt("id_jenis"), rs.getString("nama_jenis")));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data Jenis Transaksi: " + e.getMessage());
        }
    }

